export function getFunctionIdName(
  symbolSet: string,
  functionId: string
): string {
  const set = symbolSetData[symbolSet]?.mainIcons;
  if (!set) return "Unknown Function";

  const entry = set.find((item) => item.code === functionId);
  return entry ? entry.name : "Unknown Function";
}

export function findFunctionId(
  symbolSet: string,
  categoryName?: string
): string | undefined {
  if (!categoryName || !symbolSet) return undefined;

  const mainIcons = mainIconData[symbolSet];
  if (!mainIcons) return undefined;

  const normalizedCategory = categoryName.replace(/\s+/g, "_").toUpperCase();

  // Exact match first
  let icon = Object.entries(mainIcons).find(
    ([key]) => key === normalizedCategory
  );
  if (icon) return icon[1];

  // Partial match
  icon = Object.entries(mainIcons).find(([key]) =>
    key.includes(normalizedCategory)
  );
  if (icon) return icon[1];

  return undefined;
}

